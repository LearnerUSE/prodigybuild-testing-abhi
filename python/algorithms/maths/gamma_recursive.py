def test_gamma() -> None:
    assert gamma(0.5) == sqrt(pi)
    assert gamma(1) == 1.0
    assert gamma(2) == 1.0
    assert gamma(3.5) == 3.3233509704478426
    assert gamma(171.5) == 9.483367566824795e+307
    assert gamma(0) == ValueError
    assert gamma(-1.1) == ValueError
    assert gamma(-4) == ValueError
    assert gamma(172) == OverflowError
    assert gamma(1.1) == NotImplementedError